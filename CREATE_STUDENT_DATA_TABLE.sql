-- Create student_data table for registration validation
CREATE TABLE IF NOT EXISTS public.student_data (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    ht_no VARCHAR(50) NOT NULL UNIQUE,
    student_name VARCHAR(255) NOT NULL,
    year VARCHAR(20) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Enable RLS (Row Level Security)
ALTER TABLE public.student_data ENABLE ROW LEVEL SECURITY;

-- Create policy to allow authenticated users to read student data
CREATE POLICY "Allow authenticated users to read student data" ON public.student_data
    FOR SELECT USING (auth.role() = 'authenticated');

